# Ruby program to demonstrate
# the Logical Operators

# Variables
a = 10
b = 20
c = 30

# using && operator
if a == 10 && b == 20 && c == 30
	puts "Logical AND Operator: #{result = a * b * c}" 
end

# using || operator
if a == 10 || b == 20
	puts "Logical OR operator: #{result = a + b + c}"
end

# using ! operator
puts "Logical Not Operator: #{!(true)}"